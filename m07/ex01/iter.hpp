template <typename T>
void	print_value(T elem)
{
	std::cout << elem << std::endl;
	
	return ;
}

template <typename T>
void	iter(T *arr, size_t len, void (*f)(T))
{
	if (arr == NULL || f == NULL)
		return ;
	for (size_t i = 0; i < len; i++)
		(*f)(arr[i]);
	
	return ;
}

